                <h1>Ragu alla bolognese</h1>
                    Ragu alla bolognese is a meat-based sauce that originated in Bologna, Italy. 
                    It is typically served with tagliatelle pasta, although it can also be 
                    served with other types of pasta or used as a filling for lasagne.
                <h2>Ingredients:</h2>
                <ul>
                    <li>2 tablespoons olive oil (30 mL)</li>
                    <li>2 tablespoons olive oil (30 mL)</li>
                    <li>2 carrots, finely chopped</li>
                    <li>2 celery stalks, finely chopped</li>
                    <li> 500 g ground beef</li>
                    <li>500 g ground pork</li>
                    <li>240 mL dry red wine</li>
                    <li>480 mL beef broth</li>
                    <li>800 g canned crushed tomatoes</li>
                    <li>2 tablespoons tomato paste (30 mL)</li>
                    <li>Salt and black pepper to taste</li>
                    <li>240 mL whole milk</li>
                </ul>
            </div>
            <div id="recipe-right">
                <img src="../../images/Ragu-Bolognese.jpg" alt="bolognese">
            </div>
        </div>
        <div>
            <h2>Steps:</h2>    
            <ul>
                <li>Heat the olive oil in a large saucepan or Dutch oven over medium heat.</li>
                <li>Add the onion, carrots, and celery and cook until softened, about 10 minutes.</li>
                <li>Add the ground beef and pork and cook until browned, stirring occasionally.</li>
                <li>Pour in the red wine and cook until the liquid has mostly evaporated, about 10 minutes.</li>
                <li>Add the beef broth, crushed tomatoes, tomato paste, salt, and pepper. Stir well to combine.</li>
                <li>Bring the mixture to a simmer and then reduce the heat to low. Simmer for at least 2 hours, stirring occasionally.</li>
                <li>Stir in the milk and continue to simmer for an additional 30 minutes.</li>
                <li>Remove the bay leaves, if using, and serve the ragu over cooked pasta.</li>
            </ul>
